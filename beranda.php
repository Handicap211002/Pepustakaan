  <script>
    const searchBox = document.getElementById("searchBox");
    const placeholderText = document.getElementById("placeholderText");
    const searchIcon = document.getElementById("searchIcon");

    searchBox.addEventListener("input", function () {
      const hasText = searchBox.innerText.trim().length > 0;
      placeholderText.classList.toggle("hidden", hasText);
      searchIcon.classList.toggle("hidden", hasText);
    });

    searchBox.addEventListener("keydown", function (event) {
      if (event.key === "Enter") {
        event.preventDefault();
        const keyword = searchBox.innerText.trim();
        if (keyword) {
          // Redirect ke login.php dengan query string kategori
          window.location.href = `login.php?category=${encodeURIComponent(keyword)}`;
        }
      }
    });
  </script>
  <script>
  const track = document.getElementById("sliderTrack");
  const totalSlides = 3;
  const slideWidth = 1340 + 30; // gambar + gap
  let currentIndex = 0;

  setInterval(() => {
    currentIndex++;

    // Kalau sudah di slide terakhir, langsung reset ke awal
    if (currentIndex >= totalSlides) {
      track.style.transition = "none"; // matikan animasi sementara
      track.style.transform = "translateX(0px)";
      currentIndex = 0;

      // pakai timeout kecil agar animasi slide aktif lagi
      setTimeout(() => {
        track.style.transition = "transform 2s ease-in-out";
        track.style.transform = `translateX(-${slideWidth}px)`;
        currentIndex = 1;
      }, 50);
    } else {
      track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
    }
  }, 4000); // waktu antar slide (4 detik)
</script>

